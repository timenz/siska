    <h3 class="heading">Selamat Datang di Pengisian KRS Mahasiswa</h3>

    <b>1. Mahasiswa Baru (Semester Pertama)</b>
    <br> &nbsp; &nbsp; - Telah melakukan registrasi
    <br> &nbsp; &nbsp; - Pengisian KRS dilakukan oleh operator masing-masing Jurusan/Prodi

    <br><br><b>2. Mahasiswa Lama</b>
    <br> &nbsp; &nbsp; - Memenuhi persyaratan untuk dapat mengisi KRS
    <br> &nbsp; &nbsp; - Memenuhi jadwal pengisian KRS yang telah ditetapkan
    <br> &nbsp; &nbsp; - Mengisikan secara langsung ke komputer mata kuliah yang diambil sesuai jatah SKS
    <br> &nbsp; &nbsp; - Perubahan isian KRS, baik perubahan mata kuliah, hanya dapat dilakukan pada masa revisi KRS yang ditetapkan.

    <br><br><b>3. Jumlah SKS/Mata Kuliah yang Dapat Diambil</b>
    <br> &nbsp; &nbsp; - Bagi Mahasiswa Baru (semester pertama), jumlah SKS yang dapat diambil ditentukan sesuai paket mata kuliah semester 1 masing-masing Jurusan, berkisar antara 18-22 SKS.
    <br> &nbsp; &nbsp; - Bagi Mahasiswa Lama (aktif), jumlah SKS yang dapat diambil didasarkan pada matrik gabungan IP semester n-1 dan IP kumulatif.

    <br><br><b>4. Perhatian</b>
    <br> &nbsp; &nbsp; - Bila karena sesuatu hal, mahasiswa tidak dapat hadir untuk mengisi KRS, ia dapat mewakilkan kepada orang lain.
    <br> &nbsp; &nbsp; - Mahasiswa yang tidak melakukan pengisian KRS pada masa pengisian KRS dapat melakukan pengisian KRS pada masa revisi KRS.
